package jp.techarts.mining;

import static org.springframework.security.test.web.servlet.request.SecurityMockMvcRequestPostProcessors.*;
import static org.springframework.security.test.web.servlet.setup.SecurityMockMvcConfigurers.*;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

import jp.techarts.mining.prop.GetAppProperties;
import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.http.MediaType;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;
import org.springframework.test.context.web.WebAppConfiguration;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.web.context.WebApplicationContext;

@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration
@WebAppConfiguration
@SpringBootTest
public class MiningControllerMVCTest {

  @Autowired private GetAppProperties app;
  @Autowired private WebApplicationContext wac;

  private MockMvc mockMvc;

  @Before
  public void setup() {
    this.mockMvc = MockMvcBuilders.webAppContextSetup(this.wac).apply(springSecurity()).build();
  }

  @Test
  public void authorizationTest() throws Exception {
    String userName = app.getDigestUserName();
    String password = app.getDigestPassword();
    this.mockMvc
        .perform(get("/mining").accept(MediaType.TEXT_PLAIN))
        .andExpect(status().isUnauthorized())
        .andReturn();
    this.mockMvc
        .perform(get("/").accept(MediaType.TEXT_PLAIN))
        .andExpect(status().isUnauthorized())
        .andReturn();
    this.mockMvc
        .perform(post("/").with(user(userName).password(password)))
        .andExpect(status().isNotFound())
        .andReturn();
  }
}
